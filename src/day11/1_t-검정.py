'''
t검정은 통계학에서 사용하는 방법 중 하나로, 두 집단의 평균이 서로 다른지 알아보는 방법이에요.
예를 들어, A반과 B반이 있다고 생각해볼게요. A반의 학생들과 B반의 학생들이 각각 수학 시험을 봤어요.
그런데 이 두 반의 수학 점수가 평균적으로 정말로 다른지, 아니면 그냥 우연히 그렇게 나온 건지 궁금할 수 있겠죠?

이때 t검정을 사용해서 두 반의 평균 점수가 정말로 다른지 확인할 수 있어요.
쉽게 말해, t검정은 "두 집단의 평균이 정말로 다를까?"라는 질문에 답을 주는 도구라고 생각하면 돼요.

비유하자면: 친구들과 농구 경기를 했을 때, "A팀이 B팀보다 더 잘했을까?"라는 질문을 하고 싶을 때,
그냥 감으로 판단하는 대신, 실제로 득점 데이터를 가지고 비교해보는 것과 비슷해요. t검정은 그런 데이터를 비교해서 "이 두 팀의 실력이 정말 차이가 있는 걸까?"라고 판단해주는 역할을 해요.

따라서 t검정은 두 그룹의 평균이 단순히 우연히 다른 건지, 아니면 진짜로 차이가 있는지를 알 수 있게 도와주는 방법이에요!

'''

# 여기 간단한 파이썬 예제를 만들어 볼게요. 두 농구 팀 A팀과 B팀의 득점 데이터를 비교해서, t검정을 통해 두 팀의 평균 득점이 정말로 다른지 확인해보는 코드입니다.

from scipy import stats

# A팀과 B팀의 경기 득점 데이터 (가상의 데이터)
A_team_scores = [82, 88, 85, 90, 87]
B_team_scores = [78, 79, 85, 83, 80]

print(f"A팀 평균 득점: { (82+88+85+90+87)/5 }")
print(f"B팀 평균 득점: { (78+79+85+83+80)/5 }")

# t검정 수행 (독립표본 t검정, 두 집단의 데이터가 독립적일 때 사용)
t_stat, p_value = stats.ttest_ind(A_team_scores, B_team_scores)

# t검정 결과 출력
print(f"t-검정 통계량: {t_stat}")
print(f"p-값: {p_value}")

# p-값 해석 (일반적으로 p-값이 0.05보다 작으면 유의미한 차이가 있다고 판단)
if p_value < 0.05:
    print("두 팀의 평균 득점에 유의미한 차이가 있습니다.")
else:
    print("두 팀의 평균 득점에 유의미한 차이가 없습니다.")

'''
코드 설명:
A_team_scores와 B_team_scores는 A팀과 B팀의 5번 경기에서 얻은 점수예요.
np.mean()을 사용해서 각 팀의 평균 점수를 계산해요.
stats.ttest_ind() 함수를 사용해서 두 팀의 점수 데이터를 비교하는 t검정을 수행해요.
p_value는 이 차이가 우연히 나타날 확률을 의미해요. 일반적으로 p_value가 0.05보다 작다면, 두 팀의 점수 차이는 우연이 아니라 정말로 다른 것이라고 판단해요.
이 예제는 두 팀의 평균 득점이 정말 다른지 통계적으로 확인하는 방법을 보여줘요. "두 팀의 평균이 정말 다를까?"라는 질문에 답을 주는 과정이죠!
'''


'''
p-값(p-value)이란?
p-값은 두 집단의 평균 차이가 우연히 발생할 확률을 나타냅니다. 이 값은 0에서 1 사이의 숫자입니다.

p-값이 작을수록: 두 평균 차이의 우연일 가능성이 낮다는 의미입니다. 즉, 실제로 두 집단의 평균이 다를 가능성이 높다는 것이죠.
p-값이 클수록: 두 평균 차이의 우연일 가능성이 높다는 의미입니다. 즉, 두 집단의 평균이 실제로는 다르지 않을 가능성이 높다는 것입니다.


유의수준(α)과 p-값의 비교:
**유의수준 (α, 알파)**는 연구자가 미리 설정하는 기준으로, 일반적으로 0.05 또는 5%로 설정합니다.

α = 0.05일 때, 이는 "우리가 5%의 오차를 허용하겠다"는 의미입니다. 즉, 100번 중 95번은 실제 차이가 있다고 판단하고, 5번은 우연에 의해 잘못된 결론을 내릴 수 있다는 것을 허용하는 것이죠.
p-값과 유의수준 비교:

p-값 ≤ α: 두 집단의 평균 차이가 우연일 가능성이 매우 낮다고 판단하고, 귀무가설을 기각합니다. 즉, "두 집단의 평균은 실제로 다르다"라고 결론을 내릴 수 있습니다.
p-값 > α: 두 집단의 평균 차이가 우연에 불과할 가능성이 높다고 판단하고, 귀무가설을 기각하지 않습니다. 즉, "두 집단의 평균이 다르다고 볼 수 없다"라고 결론을 내립니다.


두 반(A반과 B반)의 시험 점수를 t검정으로 비교했더니, p-값이 0.03이 나왔다고 해봅시다. 만약 유의수준 α를 0.05로 설정했다면:
**p-값(0.03) ≤ α(0.05)**이므로, "두 반의 평균 점수가 우연이 아니라 실제로 차이가 있다"고 결론 내릴 수 있습니다

반대로, p-값이 0.10이 나왔다면:
**p-값(0.10) > α(0.05)**이므로, "두 반의 평균 점수 차이는 우연에 불과하다"고 결론 내리게 됩니다.

따라서, t검정을 통해 계산된 p-값을 유의수준과 비교함으로써 두 집단의 평균 차이가 우연에 불과한지, 아니면 실제로 차이가 있는지를 판단할 수 있습니다.

'''

'''
왜 t검정을 해야 할까?
t검정은 우리가 두 그룹이 정말로 다른지 확인하는 데 도움을 주는 도구예요. 
가끔 두 그룹의 평균(예를 들어, 시험 점수)이 다르게 보일 때가 있어요. 
하지만 이 차이가 진짜로 의미 있는 차이인지, 아니면 그냥 우연인지 궁금할 수 있겠죠? t검정은 이런 궁금증을 풀어주는 역할을 해요.

비유로 설명해볼게요:
두 친구가 각각 다른 방법으로 공부했어요. 첫 번째 친구는 도서관에서 공부하고,
 두 번째 친구는 집에서 공부했죠. 시험을 본 후 두 친구의 점수를 비교해봤더니, 
 도서관에서 공부한 친구가 더 높은 점수를 받았어요. 그렇다면, "도서관에서 공부하면 정말로 더 좋은 점수를 받을 수 있을까?"라고 궁금해질 거예요.

여기서 t검정을 사용하면, 그 차이가 정말 도서관에서 공부했기 때문인지, 아니면 그냥 운이 좋아서 나온 결과인지 알 수 있어요.

요약:
t검정은 두 그룹이 진짜로 다른지, 아니면 그냥 우연히 다른 것처럼 보이는지 알려주는 도구예요.
우리가 어떤 결정을 내리거나, 무언가를 비교할 때, 객관적인 판단을 도와주는 중요한 방법이랍니다!
이렇게 t검정을 하면, 더 신뢰할 수 있는 결론을 내릴 수 있어요.
'''
