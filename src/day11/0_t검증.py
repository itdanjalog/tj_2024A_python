'''
t-검정(t-test)은 두 집단 간의 평균을 비교할 때 사용되는 통계적 방법입니다.
그러나 성별에 따른 스포츠와 드라마 선호도는 범주형 데이터(예: "스포츠를 선호한다", "드라마를 선호한다")이기 때문에, t-검정보다는 카이제곱 검정이 적합합니다.
범주형 데이터를 비교할 때 t-검정을 사용하지 않고, 두 집단의 평균을 비교하는 경우에 t-검정을 사용합니다.

그럼에도 불구하고, t-검정을 사용할 수 있는 상황을 가정하려면,
예를 들어 "스포츠 선호도"와 "드라마 선호도"를 특정한 점수나 연속형 데이터로 변환해야 합니다.
예를 들어 각 사람에게 스포츠와 드라마에 대한 선호도를 1에서 10까지의 점수로 평가하게 하고, 이를 평균으로 비교할 수 있습니다.
'''

import scipy.stats as stats

# 남성과 여성의 스포츠 선호도 점수
male_sports_scores = [7, 8, 9, 6, 7]
female_sports_scores = [5, 6, 4, 7, 5]

# 독립 표본 t-검정 수행
t_stat, p_value = stats.ttest_ind(male_sports_scores, female_sports_scores)

# 결과 출력
print(f"t-통계량: {t_stat}")
# 두 집단의 평균 차이를 표준화한 값입니다. 이 값이 클수록 두 집단의 평균 차이가 크다는 것을 의미합니다.
# t-통계량의 부호: 평균 차이의 방향을 나타냅니다. 음수는 첫 번째 집단의 평균이 두 번째 집단보다 낮다는 것을, 양수는 첫 번째 집단의 평균이 두 번째 집단보다 높다는 것을 의미합니다.
print(f"p-값: {p_value}")

# 남성과 여성의 드라마 선호도 점수
male_drama_scores = [4, 5, 6, 3, 4]
female_drama_scores = [7, 8, 9, 6, 7]

# 독립 표본 t-검정 수행
t_stat, p_value = stats.ttest_ind(male_drama_scores, female_drama_scores)

# 결과 출력
print(f"t-통계량: {t_stat}")
print(f"p-값: {p_value}")

