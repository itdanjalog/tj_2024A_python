'''
**RMSE (Root Mean Squared Error, 루트 평균 제곱 오차)**는 MSE의 제곱근을 취한 값으로,
모델의 예측 성능을 평가하는 데 사용됩니다. RMSE는 MSE의 제곱근으로 계산되며,
원래 데이터의 단위와 동일한 단위를 가지므로 해석이 직관적입니다.

MSE 계산:
먼저 MSE를 계산합니다. MSE는 실제 값과 예측 값 간의 차이를 제곱하여 평균을 낸 값입니다.

제곱근 계산:
MSE의 제곱근을 구합니다. 이 과정에서 오차의 단위가 원래 단위로 변환됩니다.

특징과 장점
단위: RMSE는 원래 데이터의 단위와 동일한 단위를 가지므로 해석이 더 직관적입니다.
예를 들어, 가격을 예측하는 모델에서 RMSE는 원 단위로 표시됩니다.
해석 용이성: RMSE는 평균적인 오차 크기를 제시하므로, 모델의 예측 정확성을 이해하는 데 유용합니다.
민감성: MSE와 마찬가지로, RMSE는 큰 오차에 민감합니다. 하지만 제곱근을 취함으로써 큰 오차의 영향을 줄일 수 있습니다.

단점
아웃라이어에 민감: 큰 오차에 여전히 민감하여, 아웃라이어(극단적인 값)가 있는 경우 RMSE가 크게 증가할 수 있습니다.
정확성에 한계: 모델의 성능을 평가하는 데는 유용하지만, 단일 지표로는 모델의 모든 특성을 설명하기 어려울 수 있습니다.

'''
from sklearn.metrics import mean_squared_error
import numpy as np

# 실제 값과 예측 값
Y_true = [3, -0.5, 2, 7]
Y_pred = [2.5, 0.0, 2, 8]

# MSE 계산
mse = mean_squared_error(Y_true, Y_pred)

# RMSE 계산
rmse = np.sqrt(mse)

print(f'RMSE: {rmse:.3f}') #RMSE: 0.612

'''

**MSE (Mean Squared Error)**와 **RMSE (Root Mean Squared Error)**는 
모델의 성능을 평가하기 위해 사용되는 두 가지 주요 오차 측정 지표입니다. 
'''
