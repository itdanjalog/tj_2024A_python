'''
**R² (R-Squared, 결정 계수)**는  회귀 분석 모델의 성능을 평가하는 데 사용되는 지표로,
모델이 데이터의 변동성을 얼마나 잘 설명하는지를 나타냅니다.
R²는 모델의 예측력이 얼마나 강력한지를 측정하며, 값의 범위는 0에서 1까지입니다.

총 변동성 (Total Variance):
데이터의 실제 값이 평균값과 얼마나 차이가 나는지를 측정합니다. 이를 **총 제곱합 (Total Sum of Squares, SST)**이라고 합니다.

잔차 변동성 (Residual Variance):
모델의 예측 값과 실제 값 사이의 차이를 측정합니다. 이를 **잔차 제곱합 (Residual Sum of Squares, SSE)**이라고 합니다.

설명된 변동성 (Explained Variance):
모델이 설명할 수 있는 변동성의 양을 측정합니다. 이는 SST에서 SSE를 빼서 계산됩니다.

결정 계수 (R²):
모델이 데이터의 총 변동성 중 얼마나 많은 비율을 설명하는지를 나타냅니다.

특징과 장점
해석 용이성: R² 값이 1에 가까울수록 모델이 데이터의 변동성을 잘 설명하고 있다는 의미입니다. R² 값이 0이면 모델이 예측에 대한 설명력이 전혀 없다는 것을 의미합니다.
비율 표현: R²는 0과 1 사이의 값을 가지며, 모델이 데이터의 변동성을 몇 퍼센트 설명하는지를 비율로 나타냅니다.

단점
모델의 복잡성: R²는 모델의 복잡성을 고려하지 않습니다. 따라서 모델이 지나치게 복잡해지면 R² 값이 높아질 수 있지만, 실제로는 과적합(overfitting)일 수 있습니다.
해석의 한계: R² 값이 높다고 해서 모델이 항상 좋은 것은 아닙니다. 다른 성능 지표와 함께 고려해야 합니다.

'''
from sklearn.metrics import r2_score

# 실제 값과 예측 값
Y_true = [3, -0.5, 2, 7]
Y_pred = [2.5, 0.0, 2, 8]

# R² 계산
r2 = r2_score(Y_true, Y_pred)

print(f'R²: {r2:.3f}') # R²: 0.949


